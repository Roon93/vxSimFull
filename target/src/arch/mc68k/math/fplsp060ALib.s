/*
DESCRIPTION

This file provides the emulation of the MC68060 unimplemented floating point
instructions. The floating point instructions emulated by this library are:

	
	facos[sdx]	fasin[sdx]	fatan[sdx]	fatanh[sdx]
	fcos[sdx]	fcosh[sdx]	fetox[sdx]	fetoxm1[sdx]
	fgetexp[sdx]	fgetman[sdx]	flog10[sdx]	flog2[sdx]
	flogn[sdx]	flognp1[sdx]	fmod[sdx]	frem[sdx]
	fscale[sdx]	fsin[sdx]	fsincos[sdx]	fsinh[sdx]
	ftan[sdx]	ftanh[sdx]	ftentox[sdx]	ftwotox[sdx]

	fabs[sdx]	fadd[sdx]	fdiv[sdx]	fint[sdx]
	fintrz[sdx]	fmul[sdx]	fneg[sdx]	fsqrt[sdx]
	fsub[sdx]

The suffix s,d or x indicates the floating point format used by the
function to compute the result. The letter s is used for single precision 
format, d for double and x for extended.

The unimplemented floating point instruction emulation code comes from the
Motorola software package. The only format supported by Motorola is a
hexadecimal image of the source code. This hex image contains an entry-point
section and a code section.

The entry-point section will remain the same across new releases  so that
the calling routines will not to be re-compiled. The entry point address for a 
function is computed by adding to the hexadecimal image top address
FPLSP_060_TOP an offset.  This offset is defined by Motorola.

Nevertheless to facilited the use of this library the fplsp060Lib.h file
provides the macros to allow a direct call of the entry points.

The original Motorola file was incorporated in this file by replacing the
"dc.l" directive by ".long" and all "$" by "0x".
*/


